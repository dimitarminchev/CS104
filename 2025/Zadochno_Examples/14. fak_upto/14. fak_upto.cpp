#include <iostream>
using namespace std;
int main()
{
    // създаваме променлива n и четем число от клавиатурата в n
    int n;
    cout << "n=";
    cin >> n;

    // Създаваме променлива за произведението, като пъввоначално е 1
    int pro = 1;

    // Цикъл който умножаваме числата от 1 до n към произведението
    for (int k = 1; k <= n; k++)
    {
        pro = pro * k;
    }

    // Отпечатваме полученото произведение
    cout << "pro=" << pro << endl;

    return 0;
}
