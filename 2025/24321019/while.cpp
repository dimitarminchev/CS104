#include <iostream>
using namespace std;

int main() {
    int i = 1; // start counter i at 1
    while (i <= 10) { // loop while i is less than or equal to 10 , change if you want higher numbers than 10
        cout << i << " "; // print current number with a space after it
        i++; // increment i by 1 each loop
    }
    cout << endl; // after loop, move to a new line
}
