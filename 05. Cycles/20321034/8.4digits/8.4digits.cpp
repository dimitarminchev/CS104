//Отпечатайте тези четирицифрени числа, 
//при които цбора на цифрите на десетиците и единиците 
//е равен на цбора цифрите на хилядните и стотиците.

#include <iostream>
using namespace std;
int main()
{
	//Обхождане на всички четирицифрени
	 
	for (int i = 1000; i <= 9999; i++)
	{
		//Отпечатване на цифрите на числото
		int a = (i / 1000) % 10;
		int b = (i / 100) % 10;
		int c = (i / 10) % 10;
		int d = (i / 1) % 10;

		if (a + b == c + d) cout << i << " ";

	}
	return 0;
}