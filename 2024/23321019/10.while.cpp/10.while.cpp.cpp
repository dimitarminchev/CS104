//Цикличен алгоритъм с предусловие
#include <iostream>
using namespace std;

int main() {
    // Предусловие: броячът е инициализиран на 1
    int counter = 1;

    // Цикълът се изпълнява докато броячът не надвиши определена стойност (например 5)
    while (counter <= 100) {
        cout << counter << endl;
        counter++; // Увеличаваме брояча с 100 на всяка итерация
    }

    return 0;
}