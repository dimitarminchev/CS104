// 6.Сума на естествените числа до зададено число

#include <iostream>
using namespace std;


int main()
{
	//Неоходими променливи 
	int n ,sum = 0;

	//Въвеждане на гора граница
	cout << "n=";
	cin >> n;
	//Цикличен алгоритъм с повторение 
	for (int i = 1; i <= n; i++)
	{
		//добавяме всяко следващо i към сумата 
		sum = sum + i;


	}
	//Сума на чиската до горната граница
	cout << "sum=" << sum << "";




	return  0;
}
